void	initialize_the_end(t_data *data)
{
	int		i;
	int		j;
	char	*tab[19];

	end_names(tab);
	i = 0;
	while (i < 36)
	{
		j = 0;
		while (j < 19)
		{
			if (tab[j][i] == '0' && j < 5)
				data->the_end[j][i] = 14744576;
			else if (tab[j][i] == '0' && j < 12)
				data->the_end[j][i] = 00044576;
			else if (tab[j][i] == '0')
				data->the_end[j][i] = 06777215;
			else
				data->the_end[j][i] = 0;
			j++;
		}
		i++;
	}
}
